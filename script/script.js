function setActiveLinks(headerSelector = "header") {
  const currentPage = location.pathname.split("/").pop() || "index.html";
  const currentHash = location.hash || "#home";

  document.querySelectorAll(headerSelector + " nav a").forEach(link => {
    link.classList.remove("active"); // pehle remove kare
    const href = link.getAttribute("href");

    // Agar page match karta hai
    if (href === currentPage) {
      link.classList.add("active");
    }

    // Agar hash (#home, #about, #contact) aur index.html me hai
    else if (href.startsWith("#") && (currentPage === "index.html" || currentPage === "")) {
      if (href === currentHash) {
        link.classList.add("active");
      }
    }
  });
}

// Inline header (index.html)
document.addEventListener("DOMContentLoaded", () => {
  if (document.querySelector("header") && !document.getElementById("header")) {
    setActiveLinks("header");
  }
});


function setLang(lang) {
  document.querySelectorAll("[data-k]").forEach(el => {
    const key = el.getAttribute("data-k");
    if (translations[lang] && translations[lang][key]) {
      el.innerText = translations[lang][key];
    }
  });
}
